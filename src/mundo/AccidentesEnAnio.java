package mundo;

public class AccidentesEnAnio
{
	private int anio;
	private int accidentes;
	
	public AccidentesEnAnio(int anio, int accidentes)
	{
		this.anio = anio;
		this.accidentes = accidentes;
	}

	public int getAnio()
	{
		return anio;
	}

	public int getAccidentes()
	{
		return accidentes;
	}
}